#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	t_list	*t;
	int		i;

	i = 0;
	t = lst;
	while (lst)
	{
		lst = lst->next;
		i++;
	}
	lst = t;
	return (i);
}
